document.addEventListener('DOMContentLoaded', function () {
    document
        .querySelector('.nav_language-switcher')
        .addEventListener('click', function () {
            var dropdown = document.getElementById('languages_dropdown');
            if (
                dropdown.style.display === 'none' ||
                dropdown.style.display === ''
            ) {
                dropdown.style.display = 'block';
            } else {
                dropdown.style.display = 'none';
            }
        });
});

function openlangdropdown(params) {
    let dropdown = document.getElementById('languages_dropdown');
    dropdown.style.display = params;
}

